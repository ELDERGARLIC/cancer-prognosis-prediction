"""Stage 13 -- Raw (non-paired) bootstrap CIs backing Table 2 (tab:main).

Table 2 in the manuscript is hand-maintained directly in main.tex (unlike
Tables 3-6, which are \\input from auto-generated table_*.tex files), and three
of its rows report a raw single-model 95% bootstrap CI on external METABRIC
Harrell's C that was never previously backed by a checked-in script:

    M0 (base GNN)         0.6443 [0.624, 0.664]   (text: "2000 bootstrap resamples")
    Cox PH, elastic-net   0.6392 [0.618, 0.660]
    Cox, clinical-only    0.6400 [0.618, 0.661]

This script recomputes all three from the already-saved, frozen per-patient
external predictions (no model refit):

    M0            <- stage_5_metabric_external.json  (full_tcga_run.metabric_log_h_gnn_full)
    elastic-net   <- stage_10_baselines.json          (external.coxnet.risk)
    clinical-only <- stage_11_geneonly.json           (clinical_only_risk)

Bootstrap: standard percentile CI, B=1000, seed=42. M0's own headline CI
(Section 3.3) was generated by stage_5_metabric_external.json's own
B=2000 paired-bootstrap harness, not by this script; B=1000 here is an
independent, simpler single-model reproduction check, chosen for this
script rather than matched to that harness, and it converges to the same
CI at the stated 3-decimal precision (see `matches_manuscript` below).
This is a *raw* single-model CI, distinct from the patient-level
*paired*-difference bootstrap (B=2000) used elsewhere for Delta/CI
values -- the two must not be conflated.

No manuscript numbers change. This purely adds the missing generating code so
the repo backs up Table 2's reproducibility end to end.

Outputs:
  results/stage_13_table2_raw_cis.json
"""
from __future__ import annotations

import json
from pathlib import Path

import numpy as np
from sksurv.metrics import concordance_index_censored

SEED = 42
N_BOOT = 1000
ALPHA = 0.05

REPO = Path("/home/eldergarlic/network_share/Projects/thesis-pipelines/thesis-research-v1")
RESULTS = REPO / "output/thesis-results-repo/results"
STAGE5_JSON = RESULTS / "stage_5_metabric_external.json"
STAGE10_JSON = RESULTS / "stage_10_baselines.json"
STAGE11_JSON = RESULTS / "stage_11_geneonly.json"
OUT_JSON = RESULTS / "stage_13_table2_raw_cis.json"


def harrell(T, E, score):
    return float(concordance_index_censored(E.astype(bool), T, score)[0])


def raw_bootstrap_ci(score, T, E, n_boot=N_BOOT, seed=SEED, alpha=ALPHA):
    """Single-model (non-paired) percentile bootstrap CI on Harrell's C."""
    rng = np.random.default_rng(seed)
    n = len(T)
    point = harrell(T, E, score)
    vals = []
    for _ in range(n_boot):
        idx = rng.integers(0, n, size=n)
        if E[idx].sum() < 5:
            continue
        try:
            vals.append(harrell(T[idx], E[idx], score[idx]))
        except (ValueError, ZeroDivisionError, FloatingPointError):
            continue
    vals = np.asarray(vals)
    return {
        "point": point,
        "ci_low": float(np.quantile(vals, alpha / 2)),
        "ci_high": float(np.quantile(vals, 1 - alpha / 2)),
        "n_boot": n_boot, "n_valid": int(len(vals)), "seed": seed,
    }


def main():
    s5 = json.loads(STAGE5_JSON.read_text())
    ft = s5["full_tcga_run"]
    T = np.asarray(ft["metabric_T"], float)
    E = np.asarray(ft["metabric_E"], int)
    eta_gnn = np.asarray(ft["metabric_log_h_gnn_full"], float)

    s10 = json.loads(STAGE10_JSON.read_text())
    risk_coxnet = np.asarray(s10["external"]["coxnet"]["risk"], float)

    s11 = json.loads(STAGE11_JSON.read_text())
    risk_clin = np.asarray(s11["clinical_only_risk"], float)

    print(f"n={len(T)} events={int(E.sum())}")

    out = {"n_boot": N_BOOT, "seed": SEED, "alpha": ALPHA,
           "note": "raw single-model bootstrap CI, NOT the paired-difference "
                   "bootstrap (B=2000) used for Delta/CI values elsewhere"}

    manuscript = {
        "m0": {"point": 0.6443, "ci_low": 0.624, "ci_high": 0.664},
        "elastic_net_cox": {"point": 0.6392, "ci_low": 0.618, "ci_high": 0.660},
        "clinical_only_cox": {"point": 0.6400, "ci_low": 0.618, "ci_high": 0.661},
    }

    for key, score, label in [
        ("m0", eta_gnn, "M0 (base GNN)"),
        ("elastic_net_cox", risk_coxnet, "Elastic-net Cox"),
        ("clinical_only_cox", risk_clin, "Clinical-only Cox"),
    ]:
        r = raw_bootstrap_ci(score, T, E)
        m = manuscript[key]
        match = (abs(r["point"] - m["point"]) < 1e-3
                 and abs(r["ci_low"] - m["ci_low"]) < 1e-3
                 and abs(r["ci_high"] - m["ci_high"]) < 1e-3)
        r["manuscript_reported"] = m
        r["matches_manuscript"] = match
        out[key] = r
        print(f"{label:20s} recomputed={r['point']:.4f} [{r['ci_low']:.3f},{r['ci_high']:.3f}]  "
              f"manuscript={m['point']:.4f} [{m['ci_low']:.3f},{m['ci_high']:.3f}]  "
              f"match={match}")

    all_match = all(out[k]["matches_manuscript"] for k in ["m0", "elastic_net_cox", "clinical_only_cox"])
    out["all_match_manuscript"] = all_match
    print(f"\nAll three rows reproduce the manuscript's Table 2 CIs: {all_match}")

    OUT_JSON.write_text(json.dumps(out, indent=2))
    print(f"wrote {OUT_JSON}")


if __name__ == "__main__":
    main()
